def compute_index_of_fit(raw_data):
    n = len(raw_data)
    t = sorted(raw_data)
    F = [(i + 1 - 0.3) / (n + 0.4) for i in range(n)]

    transforms = {
        "Normal":       {"x": t,                         "y": [norm_inv(f) for f in F]},
        "Lognormal":    {"x": [math.log(v) for v in t], "y": [norm_inv(f) for f in F]},
        "Weibull":      {"x": [math.log(v) for v in t], "y": [math.log(math.log(1/(1-f))) for f in F]},
        "Eksponensial": {"x": t,                         "y": [-math.log(1-f) for f in F]},
    }

    results = {}
    for name, tr in transforms.items():
        x, y = tr["x"], tr["y"]
        sx  = sum(x);         sy  = sum(y)
        sxy = sum(xi*yi for xi,yi in zip(x,y))
        sx2 = sum(xi**2 for xi in x)
        sy2 = sum(yi**2 for yi in y)

        num = n*sxy - sx*sy
        den = math.sqrt(max((n*sx2 - sx**2) * (n*sy2 - sy**2), 0))
        rv  = num/den if den != 0 else 0
        R2  = rv**2

        b = num / (n*sx2 - sx**2) if (n*sx2 - sx**2) != 0 else 0
        a = (sy - b*sx) / n

        # ── Parameter estimation dari regresi linear ──────
        # Normal/Lognormal : yi = (xi-μ)/σ  →  b=1/σ, a=-μ/σ  →  μ=-a/b, σ=1/b
        # Weibull           : yi = β·xi - β·ln(η)  →  β=b, η=exp(-a/b)
        # Eksponensial      : yi = λ·xi  →  λ=b  (slope SELALU positif)
        params = {}
        if name == "Normal":
            params = {"μ": r2(-a/b) if b != 0 else 0,
                      "σ": r2(1/b)  if b != 0 else 0}
        if name == "Lognormal":
            # μ dan σ adalah parameter distribusi ln(T) ~ Normal(μ,σ)
            params = {"μ": r2(-a/b) if b != 0 else 0,
                      "σ": r2(1/b)  if b != 0 else 0}
        if name == "Weibull":
            params = {"β": r2(b),
                      "η": r2(math.exp(-a/b)) if b != 0 else 0}
        if name == "Eksponensial":
            # λ = laju kerusakan (positif); MTTR = 1/λ
            params = {"λ": r2(b)}

        results[name] = {
            "r": round(rv, 4),
            "R2":     r2(R2),
            "a":      r2(a),
            "b":      r2(b),
            "params": params,
        }

    best = max(results, key=lambda k: results[k]["R2"])
    return {"results": results, "best": best, "t": t, "F": F, "n": n}


def compute_gof(mod1, dtype="TTR", override_best=None):
    best  = override_best if override_best else mod1["best"]
    res   = mod1["results"][best]
    t     = mod1["t"]
    n_ttf = len(t)
    # TTF: n_fail = n+1 | TTR: n_fail = n
    n_fail = n_ttf + 1 if dtype == "TTF" else n_ttf

    # ── KS Test (Normal & Lognormal) ──────────────────────────
    if best in ("Normal", "Lognormal"):
        if best == "Normal":
            # xi = ti (tidak ditransformasi)
            # μ dan σ dari statistik sampel (ddof=1)
            x_data = list(t)
            mu_use  = sum(t) / n_ttf
            sig_use = math.sqrt(sum((ti - mu_use)**2 for ti in t) / (n_ttf - 1))
        else:
            # xi = ln(ti)
            # μ = mean sampel ln(ti), σ = 1/b dari regresi
            x_data  = [math.log(ti) for ti in t]
            mu_use  = sum(x_data) / n_ttf
            sig_use = (1 / res["b"]) if res["b"] != 0 else 1

        table = []
        for i, (ti, xi) in enumerate(zip(t, x_data)):
            Zi = (xi - mu_use) / sig_use if sig_use != 0 else 0
            Ft = norm_cdf(Zi)
            D1 = r2(Ft - i / n_fail)
            D2 = r2((i + 1) / n_fail - Ft)
            entry = {
                "i":          i + 1,
                "ti":         r2(ti),
                "xi":         r2(xi),
                "F_emp_low":  r2(i / n_fail),
                "F_emp_high": r2((i + 1) / n_fail),
                "dev_sq":     r2((ti - mu_use) ** 2),
                "Zi":         r2(Zi),
                "F_teoritis": r2(Ft),
                "D1":         D1,
                "D2":         D2,
            }
            table.append(entry)

        D_hitung = max(max(abs(row["D1"]), abs(row["D2"])) for row in table)
        D_kritis = ks_critical(n_fail)
        return {
            "method":   "Kolmogorov-Smirnov Test",
            "D_hitung": r2(D_hitung),
            "D_kritis": r2(D_kritis),
            "accept":   D_hitung <= D_kritis,
            "table":    table,
            "mu":       r2(mu_use),
            "sigma":    r2(sig_use),
        }

    # ── Bartlett Test (Eksponensial) ──────────────────────────
    if best == "Eksponensial":
        r_n      = n_ttf
        t_bar    = sum(t) / r_n
        sum_lnt  = sum(math.log(ti) for ti in t)
        mean_lnt = sum_lnt / r_n
        # Rumus Bartlett (Ebelling):
        # B = 2r [ ln(t̄) - (1/r)Σln(ti) ] / [ 1 + (r+1)/(6r) ]
        numerator   = 2 * r_n * (math.log(t_bar) - mean_lnt)
        denominator = 1 + (r_n + 1) / (6 * r_n)
        B    = numerator / denominator
        df_val   = r_n - 1   # df = k-1
        chi_low  = chi2_ppf(0.025, df_val)
        chi_high = chi2_ppf(0.975, df_val)
        # Buat tabel per baris
        table_b = []
        for i, ti in enumerate(t):
            table_b.append({
                "i":        i + 1,
                "ti":       r2(ti),
                "xi=ln(ti)": r2(math.log(ti)),
                "k+1":      r_n + 1 if i == 0 else "",
                "2k":       2 * r_n  if i == 0 else "",
                "6k":       6 * r_n  if i == 0 else "",
                "ln(Σti/k)": r2(math.log(t_bar)) if i == 0 else "",
                "Σxi(1/k)": r2(mean_lnt)          if i == 0 else "",
            })
        # Baris total
        table_b.append({
            "i":         "Total",
            "ti":        r2(sum(t)),
            "xi=ln(ti)": r2(sum_lnt),
            "k+1":       r_n + 1,
            "2k":        2 * r_n,
            "6k":        6 * r_n,
            "ln(Σti/k)": r2(math.log(t_bar)),
            "Σxi(1/k)":  r2(mean_lnt),
        })
        return {
            "method":   "Bartlett Test",
            "B":        r2(B),
            "chi_low":  r2(chi_low),
            "chi_high": r2(chi_high),
            "df":       df_val,
            "r":        r_n,
            "t_bar":    r2(t_bar),
            "mean_lnt": r2(mean_lnt),
            "table":    table_b,
            "accept":   chi_low <= B <= chi_high,
        }

    # ── Mann's Test (Weibull) ─────────────────────────────────
    if best == "Weibull":
        r_n = n_ttf
        k1  = r_n // 2
        k2  = (r_n - 1) // 2

        # Hitung Zi = ln[-ln(1 - (i-0.5)/(r+0.25))] untuk i=1..r
        Z = []
        for i in range(1, r_n + 1):
            Fi = (i - 0.5) / (r_n + 0.25)
            Z.append(math.log(-math.log(1 - Fi)))

        # Hitung Mi = Z[i+1] - Z[i] dan delta_ln_t = ln(t[i+1]) - ln(t[i])
        # untuk i = 0..r-2 (index 0-based)
        table_m = []
        ratio   = []   # delta_ln_t / Mi
        for i in range(r_n - 1):
            Mi_val      = Z[i+1] - Z[i]
            dln_t       = math.log(t[i+1]) - math.log(t[i])
            ratio_val   = dln_t / Mi_val if Mi_val != 0 else 0
            ratio.append(ratio_val)
            table_m.append({
                "i":           i + 1,
                "ti":          r2(t[i]),
                "xi=ln(ti)":   r2(math.log(t[i])),
                "1-(i-0.5)/(r+0.25)": r2(1 - (i+1-0.5)/(r_n+0.25)),
                "-ln(1-F)":    r2(-math.log(1 - (i+1-0.5)/(r_n+0.25))),
                "Zi":          r2(Z[i]),
                "Mi=Zi+1-Zi":  r2(Mi_val),
                "Δln(ti)":     r2(dln_t),
                "Δln(ti)/Mi":  r2(ratio_val),
            })
        # Baris terakhir (hanya ti dan xi, tidak ada Mi)
        table_m.append({
            "i":           r_n,
            "ti":          r2(t[-1]),
            "xi=ln(ti)":   r2(math.log(t[-1])),
            "1-(i-0.5)/(r+0.25)": r2(1 - (r_n-0.5)/(r_n+0.25)),
            "-ln(1-F)":    r2(-math.log(1 - (r_n-0.5)/(r_n+0.25))),
            "Zi":          r2(Z[-1]),
            "Mi=Zi+1-Zi":  "",
            "Δln(ti)":     "",
            "Δln(ti)/Mi":  "",
        })

        # S1 = k1 elemen pertama, S2 = k2 elemen terakhir
        S1 = sum(ratio[:k1])
        S2 = sum(ratio[k1:])
        M  = (k1 * S2) / (k2 * S1) if (k2 != 0 and S1 != 0) else float("inf")
        F_kritis = f_critical_005(k1, k2)

        return {
            "method":   "Mann's Test",
            "M":        r2(M),
            "F_kritis": r2(F_kritis),
            "k1":       k1,
            "k2":       k2,
            "S1":       r2(S1),
            "S2":       r2(S2),
            "table":    table_m,
            "accept":   M < F_kritis,
        }